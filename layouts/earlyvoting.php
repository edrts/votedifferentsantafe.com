                    <h3>ABSENTEE IN PERSON VOTING</h3>
                    <p>County Clerk's office (102 Grant Avenue) from Tuesday, October 8th through Saturday, November 2nd,
                       during regular days and hours of business (Monday through Friday 8:00 a.m. - 5:00 p.m.)
                       <strong>The office is closed Monday, October 14, 2019 in observance of Columbus Day.</strong></p>
                    <p>The County Clerk's office will be open for <strong>Voting</strong> on Saturday, November 2nd only,
                       but <strong>not</strong> the other Saturdays of early voting.</p>
                    <h3>ABSENTEE VOTING BY MAIL</h3>
                    <p>You can apply for an absentee ballot now or any time up until Friday, November 1, 2019 although we
                      encourage voters not to wait until the last minute.  The last day we can send out Absentee Ballots
                      is Friday, November 1st.</p>
                    <p>To apply for an Absentee Ballot, you can
                      <a href="https://portal.sos.state.nm.us/OVR/WebPages/AbsenteeApplication.aspx?type=RA">go online to electronically</a>
                      request an Absentee Ballot Application Request.</p>
                      <p>You can <a href="https://www.santafecountynm.gov/media/files/ABGeraldine.pdf">print out the application</a>
                        and mail or deliver it to the Couny Clerk's office.</p>
                        <p>Or call the County Clerk's Office (505-986-6280) to request an application.  When you receive the
                          application, you must fill it out and return it to the County Clerk's office.</p>
                      <h3>EARLY VOTING</h3>
                      <p>Saturday, October 19th through Saturday, November 2nd.  Hours of voting are from 12:00 Noon until 8:00 p.m.
                        Tuesday through Friday, and from 10:00 a.m. until 6:00 p.m. on Saturdays.</p>
                        <p>Early Voting Sites are located at:</p>
                        <ul>
                          <li><strong>NEW</strong> Abedon Lopez Community Center - 155A Camino de Quintana, Santa Cruz</li>
                          <li>Christian Life Church - 121 Siringo Road, Santa Fe</li>
                          <li><strong>NEW</strong> Edgewood Elementary School - 285 Dinkle Road, Edgewood</li>
                          <li>Max Coll Corridor Community Center (new facility) - 16 Avenida Torreon, Eldorado</li>
                          <li>Pojoaque County Satellite Office - 5 W. Gutierrez, Ste. 9, Pojoaque Pueblo Plaza, Pojoaque</li>
                          <li>Santa Fe County Fair Building - 3229 Rodeo Road, Santa Fe</li>
                        </ul>
