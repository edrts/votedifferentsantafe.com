                    <p>Usted puede solicitar una papeleta de voto ausente ahora o en cualquier momento hasta el viernes, 1 de noviembre de,
                      2019 A pesar de que alentar a los votantes que no esperen hasta el último minuto. El último día podemos enviar Boletas
                      en Ausencia es el viernes, 1 de noviembre.</p>
                    <p>Para solicitar un voto por correo, se puede ir
                      <a href="https://portal.sos.state.nm.us/OVR/WebPages/AbsenteeApplication.aspx?type=RA">línea de forma electrónica</a>
                      solicitar una Solicitud solicitud de voto ausente</p>
                      <p>Puede <a href="https://www.santafecountynm.gov/media/files/ABGeraldine.pdf">imprimir la solicitud</a>
                        y enviar por correo o entregarla en la oficina del Secretario Couny.</p>
                        <p>O llame a la Oficina del Secretario del Condado (505-986-6280) para pedir una solicitud. Cuando recibe
                          la solicitud, debe llenarlo y devolverlo a la oficina del Secretario del Condado.</p>
