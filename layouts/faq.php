                                <span class="english-content">
                                <p>First choice votes are counted to determine who voters like the best. If a candidate receives a majority of votes, they win. A majority is 50% of the total, plus one vote. If no candidate receives a majority, the candidate with the fewest first-choice rankings is eliminated. If your favorite candidate is eliminated, your vote is instantly counted for your next choice. This repeats until one candidate reaches a majority and wins.</p>                                
                                </span>
                                <span class="spanish-content">
                                <p>Para poder ganar una elección por Preferencia de Rango el candidato debe tener la mayoría de votos contados. La mayoría es 50% del total más uno.</p>
                                <p>Después de que todos los votos sean contados el día de la elección, el sistema contador contará todas las primeras opciones de los votantes. El candidato con menos votos, será eliminado y los candidatos en la segunda opcion recibirán esos votos.</p>
                                <p>Los votos serán recalculados y si algun candidato recibe más votos, ese candidato será declarado el ganador. Si no, el proceso continua hasta llegar con el candidato con la mayoría de votos. Ese candidato será el ganador.</p>
