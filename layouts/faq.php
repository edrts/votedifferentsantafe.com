                                <h3>How are ranked choice ballots counted?</h3>
                                </span>
                                <span class="spanish-content">
                                <h3>Como son contadas las boletas por Preferencia de Rango?</h3>
                                </span>
                            </div>
                            <div class="card-content">
                                <span class="english-content">
                                <p>To win a ranked choice election, a candidate must receive at least a majority of
                                    total votes counted. A majority is 50% of the total, plus one vote.</p>
                                <p>After all the votes are in on Election Day, counters will tally first-choice votes.
                                    If no candidate wins a majority based on first-choice votes, the candidate with the
                                    lowest number of votes is eliminated, and the second choices on that candidate’s
                                    ballots are awarded those votes.</p>
                                <p>Votes are re-calculated, and if one candidate gets a majority, they are declared the
                                    winner. If not, we repeat the process. The candidate in last place is eliminated,
                                    and the second choice on those ballots is awarded those votes. If the second choice
                                    on those ballots has already been eliminated, the third choice is counted, and so
                                    on. </p>
                                <p>The process is repeated until one candidate reaches a majority and is declared the
                                    winner.</p>
                                </span>
                                <span class="spanish-content">
                                <p>Para poder ganar una elección por Preferencia de Rango el candidato debe tener la mayoría de votos contados. La mayoría es 50% del total más uno.</p>
                                <p>Después de que todos los votos sean contados el día de la elección, el sistema contador contará todas las primeras opciones de los votantes. El candidato con menos votos, será eliminado y los candidatos en la segunda opcion recibirán esos votos.</p>
                                <p>Los votos serán recalculados y si algun candidato recibe más votos, ese candidato será declarado el ganador. Si no, el proceso continua hasta llegar con el candidato con la mayoría de votos. Ese candidato será el ganador.</p>
