<div class="container">
    <div class="row">
        <div class="col-md-10 col-md-offset-1">

            <div class="definition-section">
                <div class="definition-card">
                    <div class="card">
                        <h3>Choice</h3> means an indication on a ballot of a voter's
                        assigned ranking of candidates (i.e., first choice, second choice, third choice) for any
                        single
                        office according to the voter's preference.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Continuing ballot</h3> means a ballot that counts towards a
                        continuing candidate.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Continuing candidate</h3> means a candidate that has not been
                        eliminated.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Duplicate ranking</h3> means a voter has ranked the same
                        candidate at multiple rankings for the office being counted. The highest ranking for the
                        candidate is valid and any lower rankings of that candidate shall be ignored.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Exhausted ballot</h3> means a ballot that cannot be advanced
                        because no further continuing candidates are ranked on that ballot.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Inconsistent ranking</h3> means a voter has ranked the same
                        candidate multiple times and has placed an alternative candidate in between rankings. The
                        highest ranking for the candidate will be treated as a continuing ballot. When the lower
                        ranking
                        of that same candidate is reached, the ballot will be considered an exhausted ballot.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Majority of votes</h3> means fifty percent (50%) plus one (1) of
                        the votes cast for a particular office.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Next ranked</h3> means the highest ranked choice for a
                        continuing candidate.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Overvote</h3> means a voter has ranked more than one candidate
                        at the same ranking. If a voter gives the same ranking to more than one candidate, the
                        voter's
                        rankings shall be counted in order of preference, stopping at the point where the ballot
                        contains the same ranking for more than one candidate.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Ranked choice voting</h3> means an election system in which
                        voters rank the candidates for office in order of preference, and the ballots are counted in
                        rounds that simulate a series of runoffs until one candidate recieves a majority of votes.
                        Ranked choice voting is also known as "instant runoff voting".
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Round of counting or round</h3> means a step in
                        the counting process during which the votes for all continuing candidates are tabulated for
                        the
                        purpose of determining whether a candidate has achieved a majority of the votes cast for a
                        particular office and, absent a majority, which candidate or candidates must be eliminated.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Skipped ranking</h3> means a voter has left a ranking blank and
                        ranks a candidate in a subsequent ranking. If a voter skips a numerical ranking, the skipped
                        ranking will be ignored and the next indicated ranking on that ballot will be valid.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Undervote</h3> means a voter did not rank any candidates for an
                        office.
                    </div>
                </div>
                <div class="definition-card">
                    <div class="card"><h3>Vote</h3> means a ballot choice that is counted toward the
                        election of a candidate. During each round of counting, each continuing ballot contains one
                        vote. All first choices are votes and lower ranked choices are potential runoff votes that
                        may,
                        in accordance with the requirements of this section, become votes and subsequently credited
                        for
                        a continuing candidate.
                    </div>

                </div>


            </div>
        </div>
    </div>
</div>